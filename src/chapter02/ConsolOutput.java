package chapter02;

public class ConsolOutput {
	public static void main(String[] args) {
		System.out.println("안녕 ");   // println: 프린트 후: enter
		System.out.println("하세요");
		
		System.out.print("안녕 ");   // print: 출력
		System.out.print("하세요");
		
		System.out.println();
		
		System.out.print("안녕 \n");   //\n: enter
		System.out.print("하세요. ");
		
		System.out.println();
}
}
